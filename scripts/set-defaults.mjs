/**
 * Embed a user's exported config as the extension's default state.
 * Usage: npm run set-defaults -- path/to/homepage-YYYY-MM-DD.json
 *
 * Keys included as defaults (layout + visual — no personal notes/todo):
 *   background, palette,
 *   gs-layout-user-default, gs-layout-v, gs-hidden-widgets,
 *   rss-feeds, quick-links, shortcuts
 */

import { readFileSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const INCLUDED_KEYS = new Set([
  'background',
  'palette',
  'gs-layout-user-default',
  'gs-layout-v',
  'gs-hidden-widgets',
  'rss-feeds',
  'quick-links',
  'shortcuts',
])

const __dirname = dirname(fileURLToPath(import.meta.url))
const root      = join(__dirname, '..')

const exportFile = process.argv[2]
if (!exportFile) {
  console.error('Usage: npm run set-defaults -- <path/to/export.json>')
  process.exit(1)
}

let exported
try {
  exported = JSON.parse(readFileSync(exportFile, 'utf-8'))
} catch (e) {
  console.error(`Impossible de lire le fichier : ${exportFile}`)
  process.exit(1)
}

const defaults = {}
for (const [key, value] of Object.entries(exported)) {
  if (INCLUDED_KEYS.has(key) && value !== null && value !== undefined) {
    defaults[key] = value
  }
}

const lines = Object.entries(defaults).map(
  ([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`
).join('\n')

const output = `// Generated by: npm run set-defaults -- <export.json>
// Keys applied on first install (when localStorage is empty)

const DEFAULTS = {
${lines}
}

export function applyDefaults() {
  if (!Object.keys(DEFAULTS).length) return
  Object.entries(DEFAULTS).forEach(([key, value]) => {
    if (localStorage.getItem(key) === null && value !== null) {
      localStorage.setItem(key, value)
    }
  })
}
`

const outPath = join(root, 'src', 'config', 'defaults.js')
writeFileSync(outPath, output, 'utf-8')

console.log(`\n✓ Defaults mis à jour avec ${Object.keys(defaults).length} clés :`)
Object.keys(defaults).forEach(k => console.log(`  · ${k}`))
console.log('\n  Relance "npm run package" pour inclure ces defaults dans le zip.\n')
